	public function getOrders ($data = array()) {

		$sql = "SELECT o.order_id, CONCAT(o.firstname, ' ', o.lastname) AS customer, o.shipping_code, o.total, o.currency_code, o.currency_value, o.date_added, o.date_modified,(SELECT os.name FROM " . DB_PREFIX . "order_status os WHERE os.order_status_id = o.order_status_id AND os.language_id = '" . (int)$this->config->get('config_language_id') . "') AS order_status, fo.status as tom_status ,fo.tom_order_id FROM `" . DB_PREFIX . "order` o JOIN `".DB_PREFIX."order_product` op ON(o.order_id=op.order_id) JOIN `".DB_PREFIX."cedtom_product` fp ON(fp.product_id=op.product_id) LEFT JOIN `".DB_PREFIX."cedtom_order` fo ON(fo.order_id=o.order_id) WHERE o.order_status_id > 0";

		if (!empty($data['filter_order_id'])) {
			$sql .= " AND o.order_id = '" . (int)$data['filter_order_id'] . "'";
		}

		if (!empty($data['filter_tom_order_id'])) {
			$sql .= " AND fo.tom_order_id = '" . $data['filter_tom_order_id'] . "'";
		}

		if (!empty($data['filter_order_status'])) {
			if($data['filter_order_status'] === 'Not-Exported'){
				$sql .= " AND o.order_id NOT IN (SELECT order_id FROM ".DB_PREFIX."cedtom_order)";
			}else{
				$sql .= " AND fo.status = '" . $this->db->escape($data['filter_order_status']) . "'";
			}
		}

		if (!empty($data['filter_customer'])) {
			$sql .= " AND CONCAT(o.firstname, ' ', o.lastname) LIKE '%" . $this->db->escape($data['filter_customer']) . "%'";
		}

		if (!empty($data['filter_date_added'])) {
			$sql .= " AND DATE(o.date_added) = DATE('" . $this->db->escape($data['filter_date_added']) . "')";
		}

		if (!empty($data['filter_date_modified'])) {
			$sql .= " AND DATE(o.date_modified) = DATE('" . $this->db->escape($data['filter_date_modified']) . "')";
		}

		if (!empty($data['filter_total'])) {
			$sql .= " AND o.total = '" . (float)$data['filter_total'] . "'";
		}

		$sort_data = array(
			'o.order_id',
			'fo.tom_order_id',
			'o.customer',
			'fo.order_status',
			'o.date_added',
			'o.date_modified',
			'o.total'
		);

		if (isset($data['sort']) && in_array($data['sort'], $sort_data)) {
			$sql .= " ORDER BY " . $data['sort'];
		} else {
			$sql .= " ORDER BY o.order_id";
		}

		if (isset($data['order']) && ($data['order'] == 'DESC')) {
			$sql .= " DESC";
		} else {
			$sql .= " ASC";
		}

		if (isset($data['start']) || isset($data['limit'])) {
			if ($data['start'] < 0) {
				$data['start'] = 0;
			}

			if ($data['limit'] < 1) {
				$data['limit'] = 20;
			}

			$sql .= " LIMIT " . (int)$data['start'] . "," . (int)$data['limit'];
		}

		$query = $this->db->query($sql);

		return $query->rows;
	}

